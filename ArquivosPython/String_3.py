nome = 'Larissa Gomes Gaspar'

print(nome[0]) # --> retorna o primeiro caracter
print(nome[-1]) # --> retorna o último caracter
print(nome[:8]) # --> considera todo o início e para no caracter 7
print(nome[8:]) # --> considera a partir do caracter 8 e vai até o final
print(nome[8:20]) # --> faz uma substring pegando do caracter 8 até o 19
print(nome[8:20:2]) # --> faz uma substring pegando do caracter 8 até o 19 e pula de 2 em 2
print(nome[:]) # --> retorna a string inteira
print(nome[::-1]) # --> retorna a string inteira de trás para frente, como um espelhamento