saldo = 500 #atribuição simples
print(saldo)

saldo = 200
print(saldo)

saldo += 10  #soma
print(saldo)

saldo -= 5   #subtrai
print(saldo)

saldo //= 2  #divisão inteira --> mais usado para quando tiver só números inteiros
print(saldo)

saldo /= 2   #divide (vira float)
print(saldo)

saldo *= 10  #multiplica
print(saldo)

saldo %= 4   #resto da divisão
print(saldo)

saldo **= 2  #exponeciação
print(saldo)